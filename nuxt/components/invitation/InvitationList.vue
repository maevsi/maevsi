<template>
  <Loader :api="api">
    <div class="flex flex-col gap-4">
      <ScrollContainer
        v-if="event && invitations.length"
        class="max-h-[70vh]"
        :has-next-page="!!api.data.allInvitations?.pageInfo.hasNextPage"
        @load-more="after = api.data.allInvitations?.pageInfo.endCursor"
      >
        <table class="border border-neutral-300 dark:border-neutral-600">
          <thead
            class="sticky top-0 z-10 bg-background-bright dark:bg-background-dark"
          >
            <tr>
              <th scope="col">
                {{ t('contact') }}
              </th>
              <th />
            </tr>
          </thead>
          <tbody class="divide-y divide-neutral-300 dark:divide-neutral-600">
            <InvitationListItem
              v-for="invitation in invitations"
              :key="invitation.uuid"
              :event="event"
              :invitation="invitation"
            />
          </tbody>
        </table>
      </ScrollContainer>
      <div v-else class="flex flex-col items-center gap-2">
        {{ t('invitationNone') }}
        <FormInputStateInfo>
          {{ t('hintInviteSelf') }}
        </FormInputStateInfo>
      </div>
      <div class="flex flex-col items-center gap-1">
        <ButtonColored
          :aria-label="t('invitationAdd')"
          :disabled="
            event.inviteeCountMaximum && api.data.allInvitations?.totalCount
              ? api.data.allInvitations.totalCount >= event.inviteeCountMaximum
              : false
          "
          @click="add()"
        >
          {{ t('invitationAdd') }}
          <template #prefix>
            <IconPlus />
          </template>
        </ButtonColored>
        <p class="text-center text-gray-500 dark:text-gray-400">
          {{
            t('invitationsUsed', {
              amountCurrent: api.data.allInvitations?.totalCount,
              amountMaximum: event.inviteeCountMaximum || 'âˆž',
            })
          }}
        </p>
      </div>
      <div v-if="api.data.allInvitations?.totalCount">
        <h2>
          {{ t('feedback') }}
        </h2>
        <div class="m-auto w-3/4 sm:w-1/2 xl:w-1/3 2xl:w-1/4">
          <Doughnut
            v-if="!isTesting"
            ref="doughnutRef"
            :data="dataComputed"
            :options="options"
          />
        </div>
      </div>
      <Modal id="ModalInvitation" is-footer-hidden>
        <FormInvitation
          :event="event"
          :invitation-contact-ids-existing="invitations.map((i) => i.contactId)"
          @submit-success="store.modalRemove('ModalInvitation')"
        />
        <template #header>
          {{ t('contactSelect') }}
        </template>
      </Modal>
    </div>
  </Loader>
</template>

<script setup lang="ts">
import {
  ArcElement,
  CategoryScale,
  Chart,
  DoughnutController,
  Legend,
  LinearScale,
  Title,
  Tooltip,
} from 'chart.js'
import { consola } from 'consola'
import { Doughnut } from 'vue-chartjs'

import { useMaevsiStore } from '~/store'
import { useAllInvitationsQuery } from '~/gql/documents/queries/invitation/invitationsAll'
import { EventItemFragment } from '~/gql/generated/graphql'
import { getInvitationItem } from '~/gql/documents/fragments/invitationItem'

export interface Props {
  event: Pick<
    EventItemFragment,
    'authorUsername' | 'slug' | 'inviteeCountMaximum' | 'id'
  >
}
const props = withDefaults(defineProps<Props>(), {})

const { $colorMode } = useNuxtApp()
const { t } = useI18n()
const store = useMaevsiStore()
const config = useRuntimeConfig()

// refs
const after = ref<string>()
const doughnutRef = ref<DoughnutController>()

// api data
const invitationsQuery = await useAllInvitationsQuery({
  after,
  eventId: props.event.id,
  first: ITEMS_PER_PAGE_LARGE,
})
const api = getApiData([invitationsQuery])

// data
const isTesting = config.public.isTesting
const options = {
  plugins: {
    legend: {
      labels: {
        font: {
          fontFamily:
            'ManropeVariable, ui-sans-serif, system-ui, -apple-system, BlinkMacSystemFont, "Segoe UI", Roboto, "Helvetica Neue", Arial, "Noto Sans", sans-serif, "Apple Color Emoji", "Segoe UI Emoji", "Segoe UI Symbol", "Noto Color Emoji"',
          size: 16,
        },
      },
      onClick: () => {},
    },
  },
}

// methods
const add = () => store.modals.push({ id: 'ModalInvitation' })
const updateChart = () => {
  Chart.defaults.color = $colorMode.value === 'dark' ? '#fff' : '#000'

  if (doughnutRef.value?.chart) {
    doughnutRef.value?.chart.update()
  }
}

// computations
const dataComputed = computed(() => {
  const datasetData = [0, 0, 0]

  if (invitations.value) {
    for (const invitation of invitations.value) {
      switch (invitation.feedback) {
        case 'ACCEPTED':
          datasetData[0] += 1
          break
        case 'CANCELED':
          datasetData[1] = datasetData[1] + 1
          break
        case null:
          datasetData[2] = datasetData[2] + 1
          break
        default:
          consola.error('Unexpected invitation type.')
      }
    }
  }

  return {
    labels: [t('accepted'), t('canceled'), t('noFeedback')],
    datasets: [
      {
        data: datasetData,
        backgroundColor: ['#00FF00', '#FF0000', '#888888'],
      },
    ],
  }
})
const invitations = computed(
  () =>
    invitationsQuery.data.value?.allInvitations?.nodes
      .map((x) => getInvitationItem(x))
      .filter(isNeitherNullNorUndefined) || []
)

// lifecycle
watch(
  () => $colorMode.value,
  (_currentValue, _oldValue) => updateChart()
)
watch(
  () => doughnutRef.value?.chart,
  (_currentValue, _oldValue) => updateChart()
)

// initialization
Chart.register(
  ArcElement,
  CategoryScale,
  LinearScale,
  DoughnutController,
  Title,
  Tooltip,
  Legend
)
</script>

